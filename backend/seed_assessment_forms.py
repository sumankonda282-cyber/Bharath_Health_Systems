# ─── Seeding logic ───────────────────────────────────────────────────────────

def seed_batch(batch_name, db):
    forms = BATCHES.get(batch_name)
    if not forms:
        print(f"[seed] Unknown batch '{batch_name}' — available: {', '.join(BATCHES)}")
        return 0, 0

    inserted = skipped = 0
    for f in forms:
        exists = db.query(AssessmentForm.id).filter(
            AssessmentForm.subcategory == f['sub']
        ).first()
        if exists:
            print(f"  [skip] {f['sub']}")
            skipped += 1
            continue
        row = AssessmentForm(
            title            = f['title'],
            description      = f.get('desc', ''),
            category         = f['cat'],
            subcategory      = f['sub'],
            icon             = f.get('icon', '📋'),
            schema           = f['schema'],
            status           = 'published',
            version          = 1,
            is_template      = False,
            is_iview_enabled = False,
            created_at       = datetime.utcnow(),
            updated_at       = datetime.utcnow(),
            published_at     = datetime.utcnow(),
        )
        db.add(row)
        print(f"  [add]  {f['sub']} — {f['title']}")
        inserted += 1

    db.commit()
    return inserted, skipped


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument('--all', action='store_true', help='Seed all batches')
    parser.add_argument('--batch', help='Seed a specific batch by name')
    args = parser.parse_args()

    db = DB()
    try:
        if args.all:
            total_ins = total_skip = 0
            for name in BATCHES:
                print(f"\n── {name} ──")
                ins, skip = seed_batch(name, db)
                total_ins += ins; total_skip += skip
            print(f"\n✓ Done — inserted {total_ins}, skipped {total_skip}")
        elif args.batch:
            print(f"\n── {args.batch} ──")
            ins, skip = seed_batch(args.batch, db)
            print(f"\n✓ Done — inserted {ins}, skipped {skip}")
        else:
            print("Usage: python seed_assessment_forms.py --all  OR  --batch <name>")
            print(f"Batches: {', '.join(BATCHES)}")
    finally:
        db.close()


if __name__ == '__main__':
    main()
