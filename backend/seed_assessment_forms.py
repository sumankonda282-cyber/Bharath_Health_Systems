# ─── compact schema helpers ───────────────────────────────────────────────────

_n = [0]; _s = [0]

def fld(t, label, fid, **kw):
    _n[0] += 1
    b = {
        'id': f'f{_n[0]}', 'type': t, 'label': label, 'field_id': fid,
        'required': kw.pop('req', False), 'col_span': kw.pop('span', 1),
        'help_text': kw.pop('help', ''), 'placeholder': kw.pop('ph', ''),
        'default_value': '', 'read_only': False, 'hidden': False,
        'options': kw.pop('opts', []), 'conditions': [], 'alert_rules': [],
    }
    b.update(kw); return b

def O(*ls):
    return [{'label': l, 'value': l.lower().replace(' ','_').replace('/','_')
             .replace('-','_').replace('(','').replace(')','')
             .replace(',','').replace('.','').strip('_'), 'score_weight': 0} for l in ls]

def sec(title, *fields, na=False, cols=2):
    _s[0] += 1
    return {'id': f's{_s[0]}', 'title': title, 'layout': cols,
            'applicability_mode': 'na_allowed' if na else 'required',
            'fields': list(fields)}

def S(*secs): return {'sections': list(secs)}

# field shortcuts
def txt(l, f, **k): return fld('text', l, f, **k)
def num(l, f, **k): return fld('number', l, f, **k)
def ta(l, f, **k):  return fld('textarea', l, f, span=2, **k)
def dt(l, f, **k):  return fld('date', l, f, **k)
def dd(l, f, o, **k): return fld('dropdown', l, f, opts=o, **k)
def rb(l, f, o, **k): return fld('radio', l, f, opts=o, **k)
def cb(l, f, o, **k): return fld('checkbox', l, f, opts=o, span=2, **k)
def yn(l, f, **k):  return fld('yes_no', l, f, **k)
def sc(l, f, mn=0, mx=10, ml='None', xl='Severe', **k):
    return fld('scale', l, f, span=2, min=mn, max=mx, min_label=ml, max_label=xl, **k)
def nr(l, f, mn, mx, u='', **k):
    return fld('numeric_range', l, f, min=mn, max=mx, unit=u, **k)

